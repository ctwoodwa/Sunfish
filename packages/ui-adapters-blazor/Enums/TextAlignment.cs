namespace Sunfish.UIAdapters.Blazor.Enums;

/// <summary>
/// Horizontal text alignment used by media components (e.g.
/// <see cref="Sunfish.UIAdapters.Blazor.Components.Media.SunfishBarcode"/>).
/// </summary>
public enum TextAlignment
{
    /// <summary>Align text to the logical start (left in LTR).</summary>
    Start,

    /// <summary>Center the text horizontally.</summary>
    Center,

    /// <summary>Align text to the logical end (right in LTR).</summary>
    End,
}
