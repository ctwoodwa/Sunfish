    // ── Column Registry ─────────────────────────────────────────────────

    internal void RegisterColumn(SunfishGridColumn<TItem> column)
    {
        if (!_columns.Contains(column))
        {
            column._columnIndex = _columns.Count;
            _columns.Add(column);

            // When AutoGenerateColumns is active, re-run the reflection pass so that any
            // auto-generated column for the same field is dropped (children win — A2.4).
            if (AutoGenerateColumns)
                RefreshAutoGeneratedColumns();

            ResolveLayoutContract();
            StateHasChanged();
        }
    }

    internal void UnregisterColumn(SunfishGridColumn<TItem> column)
    {
        _columnSizingIds.Remove(column);
        _columns.Remove(column);

        // If auto-gen is active, refresh so the unregistered field's column can be regenerated.
        if (AutoGenerateColumns)
            RefreshAutoGeneratedColumns();

        ResolveLayoutContract();
        StateHasChanged();
    }

