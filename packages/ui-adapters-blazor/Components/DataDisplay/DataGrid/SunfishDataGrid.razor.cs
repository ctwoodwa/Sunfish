    protected override async Task OnParametersSetAsync()
    {
        _state.PageSize = PageSize;

        if (Page > 0)
            _state.CurrentPage = Page;

        if (SelectedItems != null)
            _selectedItems = new HashSet<TItem>(SelectedItems);

        // Materialise the highlighted-items set once per render pass for O(1) per-row lookup.
        // Use EqualityComparer<TItem>.Default so that:
        //   – reference types: reference equality (same object identity)
        //   – record / value types: value equality via GetHashCode / Equals
        // If the consumer already passes a HashSet<TItem>, use it directly to avoid an
        // unnecessary copy (the comparer is already the consumer's intended semantics).
        if (HighlightedItems is null)
        {
            _highlightedSet = null;
        }
        else if (HighlightedItems is HashSet<TItem> existingSet)
        {
            _highlightedSet = existingSet;
        }
        else
        {
            _highlightedSet = new HashSet<TItem>(HighlightedItems);
        }

        if (!_stateInitialized)
        {
            _stateInitialized = true;
            if (OnStateInit.HasDelegate)
            {
                await OnStateInit.InvokeAsync(_state);
            }
        }

        // Auto-generate columns from TItem's public properties (A2.1–A2.5).
        // Explicit <SunfishGridColumn> children take priority; auto-generated columns fill the gaps.
        // We regenerate on every parameter-set so that a change to AutoGenerateColumns is reflected.
        if (AutoGenerateColumns)
        {
            RefreshAutoGeneratedColumns();
        }
        else if (_autoColumnsGenerated)
        {
            // AutoGenerateColumns was turned off — remove any previously auto-generated columns.
            RemoveAutoGeneratedColumns();
            _autoColumnsGenerated = false;
        }

        ResolveLayoutContract();

        await ProcessDataAsync();
    }

    internal void ResolveLayoutContract()
    {
        _widthProvider = ColumnWidthProvider ?? new FixedWidthProvider();

        var visible = _visibleColumns;
        var entries = new List<ColumnSizingEntry>(visible.Count);

        for (var i = 0; i < visible.Count; i++)
        {
            var column = visible[i];
            if (!_columnSizingIds.TryGetValue(column, out var id))
            {
                id = string.IsNullOrWhiteSpace(column.Field)
                    ? $"col-{i}"
                    : $"{column.Field}-{i}";
                _columnSizingIds[column] = id;
            }

            entries.Add(new ColumnSizingEntry(
                id,
                column.EffectiveWidth,
                50,
                null,
                column.TextAlign,
                column.Locked,
                column.FrozenPosition));
        }

        _layoutContract = _widthProvider.Resolve(entries);
    }

    private string? GetSizingId(SunfishGridColumn<TItem> column)
        => _columnSizingIds.TryGetValue(column, out var id) ? id : null;

    internal string? GetResolvedColumnWidth(SunfishGridColumn<TItem> column)
    {
        var id = GetSizingId(column);
        if (id is null) return null;
        return _layoutContract.WidthById.TryGetValue(id, out var width) ? width : null;
    }

    internal string? GetColumnWidthStyle(SunfishGridColumn<TItem> column)
    {
        var width = GetResolvedColumnWidth(column);
        return width is null ? null : $"width:{width};";
    }

    internal string? GetColumnCellStyle(SunfishGridColumn<TItem> column, string? extraStyle = null)
    {
        var widthStyle = GetColumnWidthStyle(column);
        var textAlignStyle = column.TextAlign != null ? $"text-align:{column.TextAlign};" : null;
        return string.Join("", new[] { widthStyle, textAlignStyle, extraStyle }.Where(s => !string.IsNullOrEmpty(s)));
    }

    internal string? GetFrozenCellStyle(SunfishGridColumn<TItem> column, bool isHeader = false)
    {
        if (!column.Locked) return null;
        if (!_columnSizingIds.TryGetValue(column, out var id)) return null;
        if (!_layoutContract.FrozenOffsets.TryGetValue(id, out var offset)) return null;

        var side = column.FrozenPosition == GridColumnFrozenPosition.Start ? "left" : "right";
        var zIndex = isHeader ? 3 : 2;
        return $"position:sticky;{side}:{offset}px;z-index:{zIndex};background:var(--sf-color-surface);";
    }

    /// <summary>
    /// Refreshes the set of auto-generated columns from <typeparamref name="TItem"/>'s public properties.
    /// Explicit <see cref="SunfishGridColumn{TItem}"/> children (those NOT in <see cref="_autoGeneratedColumns"/>)
    /// take priority: any field already covered by an explicit column is skipped. Auto-generated columns are
    /// appended after explicit columns in <c>DisplayAttribute.Order</c> order, then declaration order.
    /// </summary>
    private void RefreshAutoGeneratedColumns()
    {
        // Remove stale auto-generated columns before regenerating.
        RemoveAutoGeneratedColumns();

        // Collect field names already claimed by explicit (non-auto-generated) columns.
        var explicitFields = _columns
            .Where(c => !string.IsNullOrEmpty(c.Field))
            .Select(c => c.Field)
            .ToHashSet(StringComparer.OrdinalIgnoreCase);

        var props = typeof(TItem).GetProperties(BindingFlags.Public | BindingFlags.Instance);
        var columnDefs = new List<(PropertyInfo Prop, int Order, string Title, bool Editable)>();

        foreach (var prop in props)
        {
            // Skip indexers and write-only properties (A2.5)
            if (prop.GetIndexParameters().Length > 0 || !prop.CanRead) continue;

            // Skip complex types — only primitives, string, Guid, DateTime*, DateOnly/TimeOnly/TimeSpan,
            // decimal, enums, and nullable variants of those are allowed (A2.5).
            var underlying = Nullable.GetUnderlyingType(prop.PropertyType) ?? prop.PropertyType;
            if (!IsSimpleType(underlying)) continue;

            // Check [Display] attribute (A2.3): skip if AutoGenerateField == false
            var displayAttr = prop.GetCustomAttribute<System.ComponentModel.DataAnnotations.DisplayAttribute>();
            if (displayAttr?.GetAutoGenerateField() == false) continue;

            // A2.4: children win — skip fields already covered by an explicit column
            if (explicitFields.Contains(prop.Name)) continue;

            var title = displayAttr?.GetName() ?? SplitCamelCase(prop.Name);
            var order = displayAttr?.GetOrder() ?? int.MaxValue;

            // Check [Editable] attribute (A2.3)
            var editableAttr = prop.GetCustomAttribute<System.ComponentModel.DataAnnotations.EditableAttribute>();
            var editable = editableAttr?.AllowEdit ?? true;

            columnDefs.Add((prop, order, title, editable));
        }

        // Append auto-generated columns after explicit columns, ordered by Display.Order then declaration order.
        foreach (var (prop, _, title, editable) in columnDefs.OrderBy(c => c.Order))
        {
            var col = new SunfishGridColumn<TItem>();
            typeof(SunfishGridColumn<TItem>).GetProperty(nameof(SunfishGridColumn<TItem>.Field))!.SetValue(col, prop.Name);
            typeof(SunfishGridColumn<TItem>).GetProperty(nameof(SunfishGridColumn<TItem>.Title))!.SetValue(col, title);
            if (!editable)
                typeof(SunfishGridColumn<TItem>).GetProperty(nameof(SunfishGridColumn<TItem>.Editable))!.SetValue(col, false);

            _columns.Add(col);
            _autoGeneratedColumns.Add(col);
        }

        _autoColumnsGenerated = true;
    }

    /// <summary>Removes all previously auto-generated columns from the column list.</summary>
    private void RemoveAutoGeneratedColumns()
    {
        foreach (var col in _autoGeneratedColumns)
        {
            _columns.Remove(col);
            _columnSizingIds.Remove(col);
        }
        _autoGeneratedColumns.Clear();
    }

    private static bool IsSimpleType(Type type) =>
        type.IsPrimitive || type.IsEnum || type == typeof(string) || type == typeof(decimal)
        || type == typeof(DateTime) || type == typeof(DateTimeOffset) || type == typeof(TimeSpan)
        || type == typeof(Guid) || type == typeof(DateOnly) || type == typeof(TimeOnly);

    private static string SplitCamelCase(string input)
    {
        if (string.IsNullOrEmpty(input)) return input;
        var result = new System.Text.StringBuilder();
        for (var i = 0; i < input.Length; i++)
        {
            if (i > 0 && char.IsUpper(input[i]) && !char.IsUpper(input[i - 1]))
                result.Append(' ');
            result.Append(input[i]);
        }
        return result.ToString();
    }

